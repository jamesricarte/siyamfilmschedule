function dropdownbutton1Clicked() {
    const Script = document.querySelector('.js-script-1');
    if (Script.style.display === 'none' || Script.style.display === '') {
        Script.style.display = 'flex';
    } else {
        Script.style.display = 'none';
    }
}

document.querySelector('.js-button-1').addEventListener('click', dropdownbutton1Clicked);

